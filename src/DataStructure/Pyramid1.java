package DataStructure;

public class Pyramid1 {

	public static void main(String[] args) {
		
		for (int a=0;a<20;a++) {
			for(int b=19;b>+a;b--) {
				System.out.print(" ");					
			}
			for(int c=0;c<=a;c++){ 
				System.out.print("* ");
			}
			
			
		{
			System.out.println("");
			}
			}
	}
	}


		

	


